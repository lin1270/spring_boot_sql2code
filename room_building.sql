CREATE TABLE `room_building` (
  `ID_` varchar(64) NOT NULL COMMENT '主键',
  `NAME_` varchar(120) NOT NULL COMMENT '名字',
  `FLOORS_` varchar(64) DEFAULT NULL COMMENT '层数',
  `HIGH_` varchar(64) DEFAULT NULL COMMENT '高度',
  `ROOM_NUM_` varchar(64) DEFAULT NULL COMMENT '户数',
  `SALE_PERMIT_` varchar(64) DEFAULT NULL COMMENT '预售证',
  `PROJECT_CODE_` varchar(64) DEFAULT NULL COMMENT '项目编码',
  `SUB_PROJECT_CODE_` varchar(64) DEFAULT NULL COMMENT '项目分期编码',
  `AREA_ID_` varchar(64) DEFAULT NULL COMMENT '小区ID',
  `AREA_NAME_` varchar(120) NOT NULL COMMENT '小区名字',
  `SALES_CENTER_ID_` varchar(64) DEFAULT NULL COMMENT '售楼部ID',
  `BUILD_SALES_CODE_` varchar(64) DEFAULT NULL COMMENT '楼盘编码',
  `PROPERTY_PRICE_` decimal(10,2) DEFAULT NULL COMMENT '物业管理费用。单位 元/平米/月。',
  `BUILDING_TYPE_` varchar(64) DEFAULT NULL COMMENT '楼型',
  `ELEVATOR_` varchar(16) DEFAULT NULL COMMENT '电梯',
  `LAT_` varchar(64) DEFAULT NULL COMMENT '纬度',
  `LNG_` varchar(64) DEFAULT NULL COMMENT '经度',
  `AGE_` int(11) DEFAULT NULL COMMENT '年代',
  `TENANT_ID_` varchar(64) DEFAULT NULL COMMENT '租户',
  `DELFLAG_` varchar(1) DEFAULT NULL COMMENT '逻辑删除标记。0-否；1-是；',
  `CREATED_BY_ID_` varchar(64) DEFAULT NULL COMMENT '创建人ID',
  `CREATED_BY_NAME_` varchar(64) DEFAULT NULL COMMENT '创建人名称',
  `CREATED_TIME_` datetime DEFAULT NULL COMMENT '创建时间',
  `MODIFIED_BY_ID_` varchar(64) DEFAULT NULL COMMENT '修改人ID',
  `MODIFIED_BY_NAME_` varchar(64) DEFAULT NULL COMMENT '修改人名称',
  `MODIFIED_TIME_` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '修改时间',
  `DISPLAY_ORDER_` int(11) DEFAULT NULL COMMENT '显示序号',
  `VERSION_` int(11) DEFAULT NULL COMMENT '版本',
  PRIMARY KEY (`ID_`),
  KEY `fk_room_building_PROJECT_CODE` (`PROJECT_CODE_`),
  KEY `fk_room_building_SUB_PROJECT_CODE` (`SUB_PROJECT_CODE_`),
  KEY `fk_room_building_AREA_ID` (`AREA_ID_`),
  KEY `fk_room_building_SALES_CENTER_ID` (`SALES_CENTER_ID_`),
  KEY `fk_room_building_BUILD_SALES_CODE` (`BUILD_SALES_CODE_`),
  CONSTRAINT `fk_room_building_AREA_ID` FOREIGN KEY (`AREA_ID_`) REFERENCES `room_living_area` (`ID_`),
  CONSTRAINT `fk_room_building_BUILD_SALES_CODE` FOREIGN KEY (`BUILD_SALES_CODE_`) REFERENCES `room_build_sales` (`CODE_`),
  CONSTRAINT `fk_room_building_PROJECT_CODE` FOREIGN KEY (`PROJECT_CODE_`) REFERENCES `room_project` (`CODE_`),
  CONSTRAINT `fk_room_building_SALES_CENTER_ID` FOREIGN KEY (`SALES_CENTER_ID_`) REFERENCES `room_sales_center` (`ID_`),
  CONSTRAINT `fk_room_building_SUB_PROJECT_CODE` FOREIGN KEY (`SUB_PROJECT_CODE_`) REFERENCES `room_project` (`CODE_`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COMMENT='楼';