CREATE TABLE `room_house_info` (
  `ID_` varchar(64) NOT NULL COMMENT '主键',
  `BUILDING_ID_` varchar(64) NOT NULL COMMENT '楼ID',
  `HOUSE_TYPE_ID_` varchar(64) NOT NULL COMMENT '户型ID',
  `HOUSE_CODE_` varchar(64) NOT NULL COMMENT '房号',
  `HOUSE_NAME_` varchar(255) NOT NULL COMMENT '房源名称',
  `DISPLAY_NAME_` varchar(255) DEFAULT NULL COMMENT '展示名称',
  `LABEL_TYPE_` tinyint(4) NOT NULL COMMENT '所属单元',
  `LABEL_` varchar(64) NOT NULL COMMENT '楼层',
  `TOTAL_PRICE_` decimal(10,2) DEFAULT NULL COMMENT '总售价',
  `UNIT_PRICE_` decimal(10,2) DEFAULT NULL COMMENT '单价',
  `DECORATION_LEVEL_` varchar(16) DEFAULT NULL COMMENT '装修程度',
  `DECORATION_` varchar(32) DEFAULT NULL COMMENT '描述',
  `IMAGES_` varchar(255) DEFAULT NULL COMMENT '图片',
  `SELL_STATUS_` varchar(3) DEFAULT NULL COMMENT '售卖状态。0-待售；1-线上销售；2-在售；3-已售。',
  `OWNER_TYPE` varchar(32) DEFAULT NULL COMMENT '权属：商品房',
  `USE_TYPE_` varchar(16) DEFAULT NULL COMMENT '用途',
  `TOWARDS_` varchar(64) DEFAULT NULL COMMENT '朝向',
  `COMPLETE_DATE_` date DEFAULT NULL COMMENT '交付时间',
  `LIVE_DATE_` date DEFAULT NULL COMMENT '入住时间',
  `TAGS_` varchar(255) DEFAULT NULL COMMENT '标签',
  `TENANT_ID_` varchar(64) DEFAULT NULL COMMENT '租户',
  `DELFLAG_` varchar(1) DEFAULT NULL COMMENT '逻辑删除标记。0-否；1-是；',
  `CREATED_BY_ID_` varchar(64) DEFAULT NULL COMMENT '创建人ID',
  `CREATED_BY_NAME_` varchar(64) DEFAULT NULL COMMENT '创建人名称',
  `CREATED_TIME_` datetime DEFAULT NULL COMMENT '创建时间',
  `MODIFIED_BY_ID_` varchar(64) DEFAULT NULL COMMENT '修改人ID',
  `MODIFIED_BY_NAME_` varchar(64) DEFAULT NULL COMMENT '修改人名称',
  `MODIFIED_TIME_` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '修改时间',
  `DISPLAY_ORDER_` int(11) DEFAULT NULL COMMENT '显示序号',
  `VERSION_` int(11) DEFAULT NULL COMMENT '版本',
  PRIMARY KEY (`ID_`),
  KEY `fk_room_house_info_BUILDING_ID` (`BUILDING_ID_`),
  KEY `fk_room_house_info_HOUSE_TYPE_ID` (`HOUSE_TYPE_ID_`),
  CONSTRAINT `fk_room_house_info_BUILDING_ID` FOREIGN KEY (`BUILDING_ID_`) REFERENCES `room_building` (`ID_`),
  CONSTRAINT `fk_room_house_info_HOUSE_TYPE_ID` FOREIGN KEY (`HOUSE_TYPE_ID_`) REFERENCES `room_house_type` (`ID_`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COMMENT='房屋';