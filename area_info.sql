CREATE TABLE `base_area_info` (
  `ID_` varchar(64) NOT NULL COMMENT 'ID',
  `AREA_NAME_` varchar(255) NOT NULL COMMENT '项目名称',
  `DISPLAY_NAME_` varchar(255) DEFAULT NULL COMMENT '全称',
  `SUMMARY_` varchar(500) DEFAULT NULL COMMENT '项目/楼盘简介',
  `ADDRESS_` varchar(255) DEFAULT NULL COMMENT '地址',
  `UNIT_PRICE_` varchar(255) DEFAULT NULL COMMENT '售卖均价',
  `TELEPHONE_` varchar(15) DEFAULT NULL COMMENT '招商电话',
  `REGION_CODE_` varchar(16) DEFAULT NULL COMMENT '区域编码',
  `SELL_STATUS_` varchar(3) DEFAULT NULL COMMENT '售卖状态：0-待售；1-在售；2-售罄。',
  `START_DATE_` date DEFAULT NULL COMMENT '开工日期',
  `END_DATE_` date DEFAULT NULL COMMENT '竣工日期',
  `SELL_START_DATE_` date DEFAULT NULL COMMENT '开盘日期',
  `SELL_END_DATE_` date DEFAULT NULL COMMENT '封盘日期',
  `COMPLETE_AREA_` varchar(120) DEFAULT NULL COMMENT '交付楼栋',
  `COMPLETE_DATE_` date DEFAULT NULL COMMENT '交付时间',
  `FLOOR_AREA_` varchar(64) DEFAULT NULL COMMENT '占地面积',
  `BUILDING_AREA_` varchar(64) DEFAULT NULL COMMENT '建筑面积',
  `SELL_AREA_` varchar(64) DEFAULT NULL COMMENT '可售面积',
  `BUILDING_COUNT_` int(11) DEFAULT NULL COMMENT '楼栋总数',
  `OWN_YEARS_` tinyint(5) DEFAULT NULL COMMENT '产权年限',
  `HOUSES_COUNT_` int(11) DEFAULT NULL COMMENT '房屋总数',
  `DECORATION_LEVEL_` varchar(32) DEFAULT NULL COMMENT '装修程度',
  `GREEN_RATE_` varchar(32) DEFAULT NULL COMMENT ' 绿化率',
  `VOLUME_RATE_` varchar(32) DEFAULT NULL COMMENT '容积率',
  `PROPERTY_COMPANY_` varchar(255) DEFAULT NULL COMMENT '物业公司',
  `PROPERTY_TYPE_` varchar(32) DEFAULT NULL COMMENT '物业类型',
  `PROPERTY_PRICE` decimal(10,2) DEFAULT NULL COMMENT '物业管理费用。单位 元/平米/月。',
  `COUNTVISIT_` int(11) NOT NULL DEFAULT '1',
  `IMAGES_` varchar(255) DEFAULT NULL COMMENT '图片',
  `TENANT_ID_` varchar(64) DEFAULT NULL COMMENT '租户',
  `DELFLAG_` varchar(1) DEFAULT NULL COMMENT '逻辑删除标记.0-否；1-是；',
  `DISPLAY_ORDER_` int(11) DEFAULT NULL COMMENT '显示序号',
  `CREATED_BY_ID_` varchar(64) DEFAULT NULL COMMENT '创建人ID',
  `CREATED_BY_NAME_` varchar(64) DEFAULT NULL COMMENT '创建人名称',
  `CREATED_TIME_` datetime DEFAULT NULL COMMENT '创建时间',
  `MODIFIED_BY_ID_` varchar(64) DEFAULT NULL COMMENT '修改人ID',
  `MODIFIED_BY_NAME_` varchar(64) DEFAULT NULL COMMENT '修改人名称',
  `MODIFIED_TIME_` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '修改时间',
  `VERSION_` int(11) DEFAULT NULL COMMENT '版本',
  PRIMARY KEY (`ID_`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COMMENT='小区基础表';